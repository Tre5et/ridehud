package net.treset.ridehud.hud;

public class TextureArea {
    public int u = 0;
    public int v = 0;
    public int xSize = 0;
    public int ySize = 0;

    public TextureArea(int u, int v, int xSize, int ySize) {
        this.u = u;
        this.v = v;
        this.xSize = xSize;
        this.ySize = ySize;
    }
}
